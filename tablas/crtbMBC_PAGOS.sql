COMMENT ON COLUMN APP_GEST_LOCALES.MBC_PAGOS.FECHA_VENCIMIENTO IS 'FECHA DE VENCIMIENTO DEL PAGO';
COMMENT ON COLUMN APP_GEST_LOCALES.MBC_PAGOS.SOPORTE IS 'SOPORTE DEL PAGO';
COMMENT ON COLUMN APP_GEST_LOCALES.MBC_PAGOS.VALOR_PAGADO IS 'VALOR PAGADO DEL PAGO';
COMMENT ON COLUMN APP_GEST_LOCALES.MBC_PAGOS.FECHA_PAGO IS 'FECHA DE PAGO DEL PAGO';
COMMENT ON COLUMN APP_GEST_LOCALES.MBC_PAGOS.ID_TIPO_PAGO IS 'TIPO DE PAGO';
COMMENT ON COLUMN APP_GEST_LOCALES.MBC_PAGOS.ID_ESTADO_PAGO IS 'ESTADO DEL PAGO';
COMMENT ON COLUMN APP_GEST_LOCALES.MBC_PAGOS.ID_LOCAL IS 'LOCAL DEL PAGO';
COMMENT ON COLUMN APP_GEST_LOCALES.MBC_PAGOS.ID_EMPRESA IS 'EMPRESA DEL PAGO';
COMMENT ON COLUMN APP_GEST_LOCALES.MBC_PAGOS.ID_USUARIO IS 'USUARIO DEL PAGO';