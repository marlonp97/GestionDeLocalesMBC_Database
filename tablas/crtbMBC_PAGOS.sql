CREATE TABLE APP_GEST_LOCALES.MBC_PAGOS
(
	ID_PAGO						NUMBER(15),
	DESCRIPCION					VARCHAR2(50)	NOT NULL,
	PRECIO           			NUMBER(12, 2)	NOT NULL,
	FECHA_VENCIMIENTO			DATE			NOT NULL,
	SOPORTE						VARCHAR2(260),
	VALOR_PAGADO				NUMBER(12, 2),
	FECHA_PAGO					DATE,
	ID_TIPO_PAGO				NUMBER(2)		NOT NULL,
	ID_ESTADO_PAGO				NUMBER(2)		NOT NULL,
	ID_LOCAL					NUMBER(15)		NOT NULL,
	ID_EMPRESA					NUMBER(15),
	ID_USUARIO					NUMBER(15)
